# Crie um algoritmo que o usuário digite 3 números e imprimi o quadrado de cada número.

numero_1 = float(input("Digite um número: "))
numero_2 = float(input("Digite um número: "))
numero_3 = float(input("Digite um número: "))

numero_1_quadrado = numero_1**2
numero_2_quadrado = numero_2**2
numero_3_quadrado = numero_3**2

print(numero_1_quadrado)
print(numero_2_quadrado)
print(numero_3_quadrado)